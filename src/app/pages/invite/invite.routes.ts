import { Routes } from '@angular/router';

/**
 * Роуты для модуля "Приглашения"
 *
 * Структура модуля:
 * - Основная страница приглашений (InviteComponent)
 *
 * URL структура:
 * /invite - Страница управления приглашениями пользователей
 *
 * Модуль включает:
 * - Отправку приглашений новым пользователям
 * - Управление активными приглашениями
 * - Просмотр статуса отправленных приглашений
 * - Повторную отправку приглашений
 * - Отзыв неиспользованных приглашений
 *
 * Модуль доступен только авторизованным пользователям
 * Компонент загружается через lazy loading для оптимизации производительности
 */
export const INVITE_ROUTES: Routes = [
	{
		path: '',
		loadComponent: async () =>
			import('./invite.component').then((c) => c.InviteComponent),
		title: 'Приглашения пользователей',
		data: {
			animation: 'animation',
		},
	},
];
